const fillTemplate = (template, variables, seed) => {
  let result = template;
  
  // –ó–∞–º–µ–Ω—è–µ–º –ø–µ—Ä–µ–º–µ–Ω–Ω—ã–µ –≤ —Ñ–∏–≥—É—Ä–Ω—ã—Ö —Å–∫–æ–±–∫–∞—Ö
  const variablePattern = /\{(\w+)\}/g;
  result = result.replace(variablePattern, (match, varName) => {
    if (variables[varName] !== undefined) {
      // –ï—Å–ª–∏ –ø–µ—Ä–µ–º–µ–Ω–Ω–∞—è - –º–∞—Å—Å–∏–≤, –≤—ã–±–∏—Ä–∞–µ–º —Å–ª—É—á–∞–π–Ω—ã–π —ç–ª–µ–º–µ–Ω—Ç
      if (Array.isArray(variables[varName])) {
        return seededChoice(variables[varName], seed + varName.length);
      }
      // –ï—Å–ª–∏ –ø–µ—Ä–µ–º–µ–Ω–Ω–∞—è - –ø—Ä–æ—Å—Ç–æ–µ –∑–Ω–∞—á–µ–Ω–∏–µ, –∏—Å–ø–æ–ª—å–∑—É–µ–º –µ–≥–æ –Ω–∞–ø—Ä—è–º—É—é
      return variables[varName];
    }
    return match;
  });
  
  return result;
};

