// –ì–µ–Ω–µ—Ä–∞—Ç–æ—Ä –ø—Å–µ–≤–¥–æ—Å–ª—É—á–∞–π–Ω—ã—Ö —á–∏—Å–µ–ª –Ω–∞ –æ—Å–Ω–æ–≤–µ seed
const seededRandom = (seed) => {
  const x = Math.sin(seed) * 10000;
  return x - Math.floor(x);
};

// –ü–æ–ª—É—á–µ–Ω–∏–µ seed –Ω–∞ –æ—Å–Ω–æ–≤–µ –¥–∞—Ç—ã
const getDailySeed = (date = new Date()) => {
  const year = date.getFullYear();
  const month = date.getMonth() + 1;
  const day = date.getDate();
  return year * 10000 + month * 100 + day;
};

// –í—ã–±–æ—Ä —ç–ª–µ–º–µ–Ω—Ç–∞ –º–∞—Å—Å–∏–≤–∞ –Ω–∞ –æ—Å–Ω–æ–≤–µ seed
const seededChoice = (array, seed) => {
  if (!array || array.length === 0) return '';
  const index = Math.floor(seededRandom(seed) * array.length);
  return array[index];
};

// –ü–æ–ª—É—á–µ–Ω–∏–µ —Ç–µ–º—ã –¥–Ω—è
export const getDailyTheme = (date = new Date()) => {
  const seed = getDailySeed(date);
  return seededChoice(gnomeMentors.dailyThemes, seed);
};

